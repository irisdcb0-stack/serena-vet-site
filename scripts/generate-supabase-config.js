const fs = require('fs');
const path = require('path');

const SUPABASE_URL = process.env.SUPABASE_URL || process.env.NETLIFY_SUPABASE_URL;
const SUPABASE_ANON_KEY = process.env.SUPABASE_ANON_KEY || process.env.NETLIFY_SUPABASE_ANON_KEY;

if (!SUPABASE_URL || !SUPABASE_ANON_KEY) {
  console.error('Missing Supabase configuration. Set SUPABASE_URL and SUPABASE_ANON_KEY as environment variables.');
  process.exit(1);
}

const config = `// Auto-generated by scripts/generate-supabase-config.js
const SUPABASE_URL = '${SUPABASE_URL.replace(/'/g, "\\'")}';
const SUPABASE_ANON_KEY = '${SUPABASE_ANON_KEY.replace(/'/g, "\\'")}';
const SUPABASE_CONFIGURED = SUPABASE_URL !== 'https://your-project-ref.supabase.co' && SUPABASE_ANON_KEY !== 'your-anon-public-key';

if (typeof window !== 'undefined') {
  window.SUPABASE_CONFIGURED = SUPABASE_CONFIGURED;
}

if (typeof window !== 'undefined' && window.supabase) {
  if (SUPABASE_CONFIGURED) {
    window.supabaseClient = window.supabase.createClient(SUPABASE_URL, SUPABASE_ANON_KEY);
    console.log('Supabase client initialized');
  } else {
    console.warn('Supabase config placeholder detected. Reemplaza SUPABASE_URL y SUPABASE_ANON_KEY.');
  }
} else {
  console.warn('Supabase SDK missing — include the CDN in your HTML');
}
`;

fs.writeFileSync(path.resolve(__dirname, '..', 'supabase-config.js'), config, 'utf8');
console.log('Generated supabase-config.js');
